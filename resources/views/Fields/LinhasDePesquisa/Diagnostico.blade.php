                        <h2><strong><span style="line-height: 1.5em; color: #563f80;">Diagnóstico</span></strong></h2>
                        <p style="line-height: 1.5em">
                            Diagnóstico
                            Muitos clientes chegam por indicações de seus médicos ou psicólogos, os quais já sabem o que
                            eles precisam. Para estes, partimos diretamente para a a apresentação dos procedimentos gerais
                            do tratamento e subsequente aplicação dos protocolos clínicos.<br><br>
                            Há, porém, os clientes que chegam sem avaliações diagnósticas atualizadas ou que têm indicação
                            de uma reavaliação ou complementação. Estes deverão passar por uma consulta médica, na qual
                            será realizada a identificação de seu perfil, queixas e aspirações, as quais irão nortear o
                            tratamento, seja este baseado em estimulação cerebral não-invasiva ou seja este baseado em
                            terapia por ondas cerebrais.<br><br>
                            Nossas avaliações são didaticamente separadas em duas modalidades, chamadas avaliações clínicas
                            e biológicas. E envolvem, respectivamente:
                            <br><br>
                            Avaliações clínicas<br>
                            Diagnóstico psiquiátrico<br>
                            Avaliação neuropsicológica<br><br>

                            Avaliações biológicas<br>
                            Mapeamento cerebral<br>
                            Endofenótipos psiquiátricos e neurológicos<br>
                            Genômica personalizada<br>





                        </p>



